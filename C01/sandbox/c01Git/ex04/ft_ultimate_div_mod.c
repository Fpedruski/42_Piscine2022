void	ft_ultimate_div_mod(int *a, int *b)
{
	int	tmp;

	if (*a == 0 || *b == 0)
	{
		*a = *a;
		*b = *b;
	}
	else
	{
		tmp = *a;
		*a = *a / *b;
		*b = tmp % *b;
	}
}
